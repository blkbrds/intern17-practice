import Foundation

final class Calculator {
    
    //MARK: - function
    func cong(a: Int, b: Int) -> Int {
        return a + b
    }
    
    func tru(a: Int, b:Int) -> Int {
        return a - b
    }
    
    func nhan(a: Int, b: Int) -> Int {
        return a * b
    }
    
    func chia(a: Int, b: Int) -> Int {
        return a / b
    }
}
